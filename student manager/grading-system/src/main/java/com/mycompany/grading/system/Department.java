    private void btn_delete_deptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete_deptActionPerformed
int row = dept_table.getSelectedRow(); 
    if (row >= 0) { 
        DefaultTableModel model = (DefaultTableModel) dept_table.getModel();
        int id = (int) model.getValueAt(row, 0); 


        int confirmation = JOptionPane.showConfirmDialog(null,
                "Are you sure you want to delete this record?",
                "Delete Confirmation",
                JOptionPane.YES_NO_OPTION);

        if (confirmation == JOptionPane.YES_OPTION) {
            String url = "jdbc:mysql://localhost:3306/my_gradingsystem";
            String username = "root";
            String password = "22703380";

            try (Connection connection = DriverManager.getConnection(url, username, password)) {
                System.out.println("Connected to the database!");

                // Use the fish_id to delete the correct record
                  GradingSystem.Department.delete_department(id, connection);
                listAll();
            } catch (SQLException ex) {
                System.err.println("Database connection error: " + ex.getMessage());
                ex.printStackTrace();
            }
        }
    } else {
        // Display a message if no row is selected
        JOptionPane.showMessageDialog(null, "Please select a row to delete.");
    }

    }//GEN-LAST:event_btn_delete_deptActionPerformed

